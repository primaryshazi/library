export namespace szdefine {
    // 个
    export const NUM_ONE: number = 1;

    // 千
    export const NUM_KILOS: number = 1000;

    // 百万
    export const NUM_MILLION: number = NUM_KILOS * NUM_KILOS;

    // 十亿
    export const NUM_BILLON: number = NUM_MILLION * NUM_KILOS;

    // 万亿
    export const NUM_TRILLON: number = NUM_BILLON * NUM_KILOS;

    // 千万亿
    export const NUM_QUADRILLON: number = NUM_TRILLON * NUM_KILOS;
}
